function soma(n1, n2) {
    return n1 + n2;
}
function subtracao(n1, n2) {
    return n1 - n2;
}
function divisao(n1, n2) {
    return n1 / n2;
}
function multiplicacao(n1, n2) {
    return n1 * n2;
}

exports.soma = soma;
exports.multiplicacao = multiplicacao;
exports.subtracao = subtracao;
exports.divisao = divisao;